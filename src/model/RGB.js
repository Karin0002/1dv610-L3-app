export class Rgb {
  /**
   * @type {number}
   */
  r
  /**
   * @type {number}
   */
  g
  /**
   * @type {number}
   */
  b

  /**
   * @param {number} red 
   * @param {number} green 
   * @param {number} blue 
   */
  constructor (red, green, blue) {
    this.r = red
    this.g = green
    this.b = blue
  }
}
